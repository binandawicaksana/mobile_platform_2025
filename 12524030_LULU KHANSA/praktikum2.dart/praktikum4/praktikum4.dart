void loopingPertama() {
  print("LOOPING PERTAMA");
  for (var i = 2; i <= 20; i += 2) {
    print("$i - I Love Byeon Woo Seok");
  }
}

void loopingKedua() {
  print("LOOPING KEDUA");
  for (var i = 20; i >= 2; i -= 2) {
    print("$i - I Will Become Woo Seok's life partner >_<");
  }
}

void main() {
  loopingPertama();
  loopingKedua();
}

        